    private void jTextFieldsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldsearchKeyReleased
        // TODO add your handling code here:
        try {
            String data = jTextFieldsearch.getText();
            String sql = "Select sale.id_product ,pharmacy.name_product,sale.lot_product,sale.number_product"
                    + " from sale INNER JOIN pharmacy "
                    + "ON sale.id_product = pharmacy.id_product "
                    + "where sale.id_product=?";
            pst = con.prepareStatement(sql);
            pst.setString(1, data);
            rs = pst.executeQuery();
            tbproduct.setModel(DbUtils.resultSetToTableModel(rs));
            System.out.println(sql);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextFieldsearchKeyReleased

    private void jTextFieldnumberKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldnumberKeyReleased
        //String getid = jTextFieldid.getText();
        String sql = "select * from pharmacy where id_product='" + jTextFieldid.getText() + "'";
        int price = 0;
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                x = rs.getString("price_product");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form2.class.getName()).log(Level.SEVERE, null, ex);
        }
        ans = Double.valueOf(x) * Double.valueOf(jTextFieldnumber.getText());
        //df.format(ans);
        jTextFieldprice.setText(Double.toString(ans));

    }//GEN-LAST:event_jTextFieldnumberKeyReleased

    private void tbproductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbproductMouseClicked
        try {
            // TODO add your handling code here:
            int row = tbproduct.getSelectedRow();
            String selectid = tbproduct.getValueAt(row, 0).toString();
            String sql = "select * from pharmacy where id_product='" + selectid + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("id_product");
                jTextFieldid.setText(add1);
                String add2 = rs.getString("name_product");
                jTextFieldname.setText(add2);
            }
            //jTextFieldid.setText(selectid);
        } catch (SQLException ex) {
            Logger.getLogger(Form2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbproductMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (tbsave.isRowSelected(selected)) {
            double moneySelected = Double.valueOf(tbsave.getValueAt(selected, 3).toString().replace(",", ""));
            System.out.println("แสดงจำนวนเงิน" + moneySelected);
            total -= moneySelected;
            lbtotal.setText("ราคารวม  : " + df.format(total) + " บาท");
            if (total == 0) {
                lbtotal.setText("ราคารวม  : 0.00 บาท");
            }
            model.removeRow(selected);
        } else {
            JOptionPane.showMessageDialog(this, "กรุณาเลือกแถวที่ต้องการลบ", "คำเตือน", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTextFieldid.getText().isEmpty() || jTextFieldname.getText().isEmpty() || jTextFieldnumber.getText().isEmpty() || jTextFieldprice.getText().isEmpty() || jTextFielddate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "กรุณาใส่ข้อมูลให้ครบ", "คำเตือน", JOptionPane.ERROR_MESSAGE);
        } else {
            String[] data = {jTextFieldid.getText(), jTextFieldname.getText(), jTextFieldnumber.getText(), jTextFieldprice.getText(), jTextFielddate.getText()};
            total += ans;
            model.addRow(data);
            lbtotal.setText("ราคารวม  : " + df.format(total) + " บาท");
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

