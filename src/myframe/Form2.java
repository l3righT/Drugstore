    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {

            int rows = tbsave.getRowCount();

            for (int row = 0; row < rows; row++) {
                String idsale = (String) tbsave.getValueAt(row, 0);
                String numbersale = (String) tbsave.getValueAt(row, 2);
                String datesale = (String) tbsave.getValueAt(row, 4);
                String sql = "Insert into savesale(id_sale,number_sale,date_sale) values ('" + idsale + "','" + numbersale + "','" + datesale + "')";

                pst = con.prepareStatement(sql);
                pst.execute();
            }
            JOptionPane.showMessageDialog(null, "Successfully Save");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        jTextFieldsearch.setText("");
            jTextFielddate.setText("");
            jTextFieldid.setText("");
            jTextFieldname.setText("");
            jTextFieldnumber.setText("");
            jTextFieldprice.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

